# Operadores

print(3 + 2) # Suma
print(3 - 2) # Resta
print(3 * 2) # Multiplicación
print(3 / 2) # División
print(3 % 2) # Módulo
print(3 ** 2) # Potencia
print(3 // 2) # División entera

# Operadores de comparación
print(3 > 2) # Mayor que
print(3 < 2) # Menor que
print(3 >= 2) # Mayor o igual que
print(3 <= 2) # Menor o igual que
print(3 == 2) # Igual que
print(3 != 2) # Diferente que

# Operadores lógicos
print(True and True) # and
print(True or False) # or
print(not True) # not

# Operadores de identidad
print(3 is 3) # is
print(3 is not 3) # is not

# Operadores de membresía
print("a" in "Hola") # in
print("a" not in "Hola") # not in

