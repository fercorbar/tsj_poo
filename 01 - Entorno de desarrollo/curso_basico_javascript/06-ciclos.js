// Ciclos o bucles

// for
// while
// do while
// for in
// for of
// examples
for (let i = 0; i < 10; i++) {
    console.log(i);
}
let j = 0;
while (j < 10) {
    console.log(j);
    j++;
}

let k = 0;
do {
    console.log(k);
    k++;
}
while (k < 10);

let l = [1, 2, 3, 4, 5];
for (let m in l) {
    console.log(m);
}
