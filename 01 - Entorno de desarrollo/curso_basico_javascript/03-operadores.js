// Operadores

// Operadores aritméticos
let a = 5;
let b = 2;
let suma = a + b;
let resta = a - b;
let multiplicacion = a * b;
let division = a / b;
let modulo = a % b;
console.log(suma, resta, multiplicacion, division, modulo);

// Operadores de comparación
let c = 5;
let d = 2;
let mayorQue = c > d;
let menorQue = c < d;
let mayorIgualQue = c >= d;
let menorIgualQue = c <= d;
let igual = c == d;
let diferente = c != d;
console.log(mayorQue, menorQue, mayorIgualQue, menorIgualQue, igual, diferente);

// Operadores lógicos
let e = 5;
let f = 2;
let and = e > 4 && f > 1;
let or = e > 4 || f > 1;
let not = !(e > 4);
console.log(and, or, not);

// Operadores de asignación
let g = 5;
let h = 2;
g += h;
h -= g;
console.log(g, h);

// Operadores de incremento y decremento
let i = 5;
let j = 2;
i++;
j--;
console.log(i, j);

// Operadores de concatenación
let k = 'Hello';
let l = 'World';
let m = k + ' ' + l;
console.log(m);

// Operadores condicionales
let n = 5;
let o = 2;
let p = n > o ? 'Si' : 'No';
console.log(p);

// Operadores de tipo
let q = 5;
let r = '5';
let s = q == r;
let t = q === r;
console.log(s, t);



